# Dictionary in Python 

# A Dictionary is a collection of key-value pairs.

# Features of Dictionary
'''
Ordered 
Mutable (can be modified)
Keys must be unique
Values can be duplicated
Fast data lookup using keys
'''

d = {1:"A",2:"B", 3:"C", 4:"D"}

print(d)
print(type(d))

# Output :

# {1: 'A', 2: 'B', 3: 'C', 4: 'D'} 
# <class 'dict'>