# Function with Arguments in Python

# Information can be passed to functions as arguments. Arguments are specified after the function name, inside the parentheses. You can add as many arguments as you want, just separate them with a comma.


# Creating a Function with Arguments

def my_function(name):
    print(name + " hello")        # name is the argument passed to the function my_function. When we call the function with an argument, it will print the value of name followed by " hello".

    
# Calling a Function with Arguments
my_function("Yatharth")         # When we call the function with the argument "Yatharth", it will print "Yatharth hello". Because we passed "Yatharth" as the value for the parameter name, the function uses that value when it executes.

