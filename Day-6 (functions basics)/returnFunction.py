# Return Function in Python

#Return function is used to return values from a function

# Creating a Function with Return Value

def my_function(x):
    return 5 * x        # The return statement is used to exit a function and go back to the place where it was called. It can also be used to return a value from the function. In this case, the function takes an argument x and returns the value of 5 times x.

# Calling a Function with Return Value
result = my_function(3)         # When we call the function with the argument 3, it will return the value of 5 times 3, which is 15. We can store this returned value in a variable called result.

print(result)                 # Finally, we print the value of result, which will output 15.

# You can also return multiple values from a function by separating them with commas. 


