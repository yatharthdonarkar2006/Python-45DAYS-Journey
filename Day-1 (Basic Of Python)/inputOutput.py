# INPUT AND OUTPUT IN PYTHON

#Input 

# We Use the input function to take input from the user and print function to print the output on the console
# input() function takes input from the user and returns it as a string. We can also specify a prompt message to be displayed to the user while taking input.
# input is a keyword use to take the input from the user and store in the variable.

Name = input("Enter Your Name : ")     #Taking Input from the user and storing it in a variable.
age = input("Enter Your Age : ")           #Taking Input from the user and storing it in a variable.

#OUTPUT

# We use the Keyword Print to print the output on the console. We can also specify multiple values to be printed by separating them with a comma.
# The print function will automatically add a space between the values.
#the text inside the double quotes is called a string and it is a sequence of characters. We can also use single quotes to define a string.
# the string which is written without Quotes are the variables which are used to store the values and we can print the values of the variables using the print function.

print("My Name is",Name ,"and I am",age,"years old.")   #Printing the values of the variables using print function.

# OUTPUT WILL BE :
# Enter Your Name : Yatharth Donarkar
# Enter Your Age : 20