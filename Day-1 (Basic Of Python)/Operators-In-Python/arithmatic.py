# 1. Arithmetic Operators

a = 10
b = 20 

print (a+b)   # Addition Operator                                           output will be 30
print (a-b)   # Subtraction Operator                                        output will be -10
print (a*b)   # Multiplication Operator                                     output will be 200
print (a/b)   # Division Operator                                           output will be 0.5
print (a//b)  # Floor Division Operator (Removes decimal part)              output will be 0
print (a%b)   # Modulus Operator                                            output will be 10
print (a**b)  # Exponentiation (Power) Operator                             output will be 10000000000
