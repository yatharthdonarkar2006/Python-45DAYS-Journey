# Membership Operators in Python
# Membership Operators are used to Test if a Sequence is Presented in an Object.

"""
( in )        operator returns True if a Sequence with the Specified Value is Present in the Object, otherwise it returns False.
( not in )    operator returns True if a Sequence with the Specified Value is Not Present in the Object, otherwise it returns False.
"""
a = "Hello World"

print ("Hello" in a)  # Output will be True
print ("Python" in a)  # Output will be False
print ("Hello" not in a)  # Output will be False
print ("Python" not in a)  # Output will be True
