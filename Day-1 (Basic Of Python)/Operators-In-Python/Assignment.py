# Assignment Operator in Python  (8 Types of Assignment Operators in Python)

"""
= is the Assignment Operator in Python. It is used to Assign a Value to a Variable.
+= addition and assignment the value
-= Subtr5act And Assignment the value
*= Multiply And Assignment the value
/= Divide And Assignment the value
//= Floor Division And Assignment the value
%= Modulus And Assignment the value
**= Exponentiation(POWER) And Assignment the value

"""

a = 10
print (a)   # Output will be 10

a += 5
print (a)   # Output will be 15

a -= 3
print (a)   # Output will be 12

a *= 2
print (a)   # Output will be 24

a /= 4
print (a)   # Output will be 6.0

a //= 2
print (a)   # Output will be 3.0

a %= 2
print (a)   # Output will be 1.0

a **= 3
print (a)   # Output will be 1.0
