# Identifer in Python

# Identifiers are names used to identify variables, functions, classes, modules, and other objects in Python. They are user-defined names that follow certain rules and conventions.

# Rules for Identifiers:
# 1. An identifier can only contain letters (a-z, A-Z), digits (0-9), and underscores (_).
# 2. An identifier must start with a letter or an underscore, but cannot start with a digit.
# 3. Identifiers are case-sensitive, meaning that "myVariable" and "myvariable" are considered different identifiers.
# 4. Identifiers cannot be the same as Python keywords (reserved words).
# 5. There is no limit on the length of an identifier, but it is recommended to keep it reasonable for readability.

# Examples of valid identifiers:
my_variable = 10                  #my_variable is a identifier
def my_function():                #my_function is a identifier
    pass
class MyClass:                    #MyClass is a identifier
    pass

# Examples of invalid identifiers:
# 1variable = 10  # Invalid: starts with a digit    
# my-variable = 10  # Invalid: contains a hyphen
# my variable = 10  # Invalid: contains a space
# 2myFunction()  # Invalid: starts with a digit
# my-function()  # Invalid: contains a hyphen

# It's important to choose meaningful and descriptive identifiers to improve the readability of your code. For example, instead of using "x" as a variable name, you could use "age" if it represents a person's age.


