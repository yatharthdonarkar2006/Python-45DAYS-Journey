# Type Conversion in Python

# 1. Implicit Type Conversion (Type Coercion)

# In implicit type conversion, Python automatically converts one data type to another when performing operations that involve 
# different data types. This is done to prevent errors and ensure that the operation can be performed successfully.

# Example of implicit type conversion
x = 10        # Integer
y = 3.14      # Float
result = x + y  # Python converts 'x' to float before performing the addition

print(result)  # Output: 13.14

