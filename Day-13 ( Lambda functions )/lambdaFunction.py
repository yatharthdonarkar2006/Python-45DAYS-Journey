# Lambda Function in Python

# A lambda function is a small, anonymous (nameless) function that can take any number of arguments but contains only one expression and automatically returns its result

# lambda keyword is used

#Can have any number of arguments.
#Contains only one expression.
#Automatically returns the result of that expression.

# syntax 

"""
    lambda arguments: expression
"""

# Example 

sum = lambda x : x*x

print (sum(4))