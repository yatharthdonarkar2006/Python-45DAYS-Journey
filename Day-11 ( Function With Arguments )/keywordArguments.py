# Keyword Arguments in Python 

# Keyword arguments are a way to pass arguments to a function by explicitly specifying the parameter name and its corresponding value.
# This allows you to provide arguments in any order, as long as you specify the parameter names.

# Example of Keyword Arguments

def my_function(fname, lname):
    print(fname + " " + lname)
    
# Calling the function with keyword arguments

my_function(fname="Yatharth", lname="Donarkar")

# Output: Yatharth Donarkar