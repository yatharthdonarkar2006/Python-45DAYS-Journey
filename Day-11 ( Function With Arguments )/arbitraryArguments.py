# Arbitrary Arguments (*args) in Python

# Arbitrary arguments allow you to pass a variable number of arguments to a function.
# In Python, you can use the *args syntax to indicate that a function can accept any number of positional arguments.

# Arbitrary Arguments (*args) is used when the number of Argument is unknown.

# It is often used in function definitions to allow for more flexible function calls.

# Example of Arbitrary Arguments (*args)

def my_function(*num):
    print(num)
    
my_function(12, 13, 14, 15)
    

