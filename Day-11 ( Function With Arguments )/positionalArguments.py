# Positional Arguments in Python 

# Positional arguments are the most common type of arguments in Python. When you call a function, the values you pass are assigned to the parameters in the order they are defined in the function.

# Example of Positional Arguments

def my_function(fname, lname):
    print(fname + " " + lname)
    
# Calling the function with positional arguments

my_function("Yatharth", "Donarkar")

# Output: Yatharth Donarkar