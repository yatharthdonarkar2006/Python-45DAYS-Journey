# Accessing Tuple Elements in Python 

# we can use positive or negative indexing here to accessing tuple elements 

# With Positive Indexing

t = (10, 20, 30, 40, 50)

print(t[0])
print(t[1])
print(t[2])
print(t[3])
print(t[4])

# Output : 
'''
10
20
30
40
50

'''
# With Negative Indexing
# Negative indexing starts from the last index and it is [-1]

print(t[-1])
print(t[-2])
print(t[-3])
print(t[-4])
print(t[-5])

# Output : 
'''
50
40
30
20
10

'''