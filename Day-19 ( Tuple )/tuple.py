# Tuple in Python 

# A Tuple is an ordered and immutable (cannot be changed) collection of elements.

# Features of Tuple
'''
Ordered
Immutable
Allows duplicate values
Supports indexing and slicing
Faster than lists
'''

t = (10, 30, 20, 5)

print(type(t))
print(t)

# Output :
# <class 'tuple'>
# (10, 30, 20, 5)