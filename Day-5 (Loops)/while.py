# while loop in Python

# while loop is used to execute a block of code repeatedly as long as a given condition is true.

# Syntax:
# while condition:
#     # code block to be executed

# Example: Using while loop to print numbers from 1 to 5
i = 1
while i <= 5:
    print(i)
    i += 1  # Incrementing the value of i by 1
    
    
    #OUTPUT
    """
    1
    2
    3
    4
    5
    """
    
    