# if else in python 

# if else statement is used to execute a block of code if a condition is true, and another block of code if the condition is false.

# syntax of if else statement in python

# if condition:
#     # block of code to be executed if the condition is true
# else:
#     # block of code to be executed if the condition is false

# example of if else statement in python

age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")
    
# in this example, the condition is age >= 18. If the condition is true, the code block under the if statement will be executed, and it will print "You are an adult." If the condition is false, the code block under the else statement will be executed, and it will print "You are a minor."
